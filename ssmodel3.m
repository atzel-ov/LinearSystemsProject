function dx = ssmodel3(t,x)

A = [ -0.0507   -3.861      0    -32.2
      -0.00117  -0.5164     1       0
      -0.000129  1.4168  -0.4932    0
          0        0        1       0];

B = [ 0
     -0.0717
     -1.645
      0];

C = [0 0 1 0];

p1 = -1.25 + (2.2651)*1i;
p2 = -1.25 - (2.2651)*1i;
p3 = -0.01 + (0.095)*1i;
p4 = -0.01 - (0.095)*1i;

K = place(A,B,[p1,p2,p3,p4]);


op1 = -0.1;
op2 = -0.421;
op3 = -0.587;
op4 = -1;

L = place(A',C',[op1 op2 op3 op4])';

u = -K*x(5:8);


dx1 = A*x(1:4) + B*u;
dx2 = A*x(5:8) + B*u + L*(x(3)-x(7));
dx = [dx1;dx2];